// using =
let x = 10;
console.log("initial value of x:",x);

// using += (x=x+5)
x+=5;
console.log("after x+=5:",x);

// using -= (x=x-5)
x-=5;
console.log("after x-=5:",x);

// using *= (x=x*5)
console.log("after x*=5:",x);

// using /= (x=x/5)
console.log("after x/=5:",x);

// using %= (x=x%5)
console.log("after x%=5:",x);


